/**
 * Scrape proxies from all sources and save to file.
 * @param {Function} progressCallback - optional (processed, total, found)
 * @param {string} outputFile - path to save proxies
 * @returns {Promise<number>} number of unique proxies saved
 */
async function scrapeProxies(progressCallback, outputFile = 'proxy.txt') {
  const proxies = new Set();
  let processed = 0;
  const total = PROXY_SOURCES.length;

  for (const url of PROXY_SOURCES) {
    try {
      const response = await axios.get(url, { timeout: 10000 });
      if (response.status === 200) {
        const lines = response.data.split('\n');
        for (const line of lines) {
          const trimmed = line.trim();
          if (trimmed.includes(':')) {
            const [ip, port] = trimmed.split(':', 2);
            if (ip.split('.').length === 4 && !isNaN(parseInt(port))) {
              proxies.add(`${ip}:${port}`);
            }
          }
        }
      }
    } catch {
      // ignore failed sources
    }
    processed++;
    if (progressCallback) {
      await progressCallback(processed, total, proxies.size);
    }
  }

  const proxyList = Array.from(proxies);
  const outputPath = path.resolve(outputFile);
  await fs.writeFile(outputPath, proxyList.join('\n'));
  return proxyList.length;
}

